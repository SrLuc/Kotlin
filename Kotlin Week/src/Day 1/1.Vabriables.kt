// Kotlin Week/Day 1
// This is a single-line comment in Kotlin


// Variable Declaration without type inference
val name = 'John Doe' //String
var age = 30 //Int
val height = 5.9 //Double
var isEmployed = true //Boolean


// Data Types with type inference
val integerValue: Int = 100 // Integer
val doubleValue: Double = 10.5 // Double
val stringValue: String = "Hello, Kotlin!" // String
val booleanValue: Boolean = true // Boolean
val charValue: Char = 'A' // Character


// Mutable and Immutable Variables
val immutableValue = 10 // Immutable variable
var mutableValue = 20 // Mutable variable


// String Interpolation
val firstName = "John"
val lastName = "Doe"
val greeting = "Hello, my name is $firstName $lastName"
println(greeting) // Output: Hello, my name is John Doe


// String Templates
val age = 30
val message = "I am $age years old"
println(message) // Output: I am 30 years old


